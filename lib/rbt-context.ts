export const RBT_TASK_LIST = `
## RBT Task List 3.0 (BACB)

### Section 1: Measurement
1A. Prepare for data collection
1B. Implement continuous measurement (frequency, rate, duration, latency, IRT)
1C. Implement discontinuous measurement (whole interval, partial interval, momentary time sampling)
1D. Implement permanent product recording
1E. Enter data and update graphs

### Section 2: Skill Acquisition
2A. Prepare for skill acquisition sessions
2B. Implement discrete trial teaching (DTT)
2C. Implement naturalistic environment teaching (NET)
2D. Implement task analysis and chaining
2E. Implement discrimination training
2F. Implement stimulus control transfer procedures
2G. Implement prompt and prompt fading procedures
2H. Implement generalization and maintenance procedures
2I. Implement shaping

### Section 3: Behavior Reduction
3A. Identify antecedents to target behaviors
3B. Implement interventions based on modification of antecedents
3C. Implement differential reinforcement procedures (DRA, DRO, DRI, DRL)
3D. Implement extinction procedures
3E. Implement crisis/emergency procedures

### Section 4: Documentation and Reporting
4A. Maintain session notes
4B. Report other variables that might affect the client
4C. Generate objective session summaries

### Section 5: Professional Conduct and Scope of Practice
5A. Describe the role of the RBT in the treatment team
5B. Respond appropriately to feedback from supervisors
5C. Communicate with stakeholders within the RBT scope of practice
5D. Maintain professional boundaries
5E. Maintain client dignity
`

