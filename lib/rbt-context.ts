// Core ABA terminology used in simulator scenarios
// ─────────────────────────────────────────────────────────────────────────────
export const CLINICAL_DEFINITIONS = `
## Key Clinical Definitions

### Measurement
- Frequency/Rate: count of behavior per time unit
- Duration: total time behavior occurs
- Latency: time from stimulus to response onset
- IRT: time between consecutive responses
- Continuous measurement: records every occurrence
- Whole interval: scored correct only if behavior occurs entire interval
- Partial interval: scored correct if behavior occurs any part of interval
- Momentary time sampling: scored if behavior occurring at moment of observation

### Reinforcement
- Positive reinforcement (+R): add stimulus to increase future behavior
- Negative reinforcement (-R): remove stimulus to increase future behavior
- Continuous (CRF): every response reinforced (use during acquisition)
- Intermittent: some responses reinforced (use during maintenance)
- Fixed ratio (FR), Variable ratio (VR), Fixed interval (FI), Variable interval (VI)

### Functions of Problem Behavior (SEAT)
- Social positive: attention, access to tangibles
- Social negative: escape/avoidance
- Automatic: sensory (not socially mediated)

### Prompting Hierarchy
- Most-to-least (errorless): start with most intrusive, fade systematically
- Least-to-most: start with least intrusive, increase only if no response after wait time
- Prompt types (most → least intrusive): physical > model > verbal > gesture > positional
- 3-second response interval before adding or increasing prompt level

### Chaining
- Forward chaining: teach first step first; prompt remaining steps
- Backward chaining: teach last step first; prompt preceding steps
- Total task: all steps taught in every trial

### Differential Reinforcement
- DRA: reinforce alternative behavior; withhold for target behavior
- DRI: reinforce behavior incompatible with target behavior
- DRO: reinforce absence of target behavior during interval
- DRL: reinforce lower rates of target behavior

### Extinction
- Withholding reinforcer that previously maintained behavior
- Extinction burst: temporary increase in frequency, intensity, or variability
- Spontaneous recovery: temporary reappearance of extinguished behavior

### Crisis Intervention
- Prioritize safety of client and others at all times
- Follow organization safety protocols
- Call for supervisor assistance
- Document incident fully
- Never implement restraint unless trained and approved in behavior plan

### Scope of Practice
- RBTs implement plans designed and supervised by BCBA/BCaBA
- RBTs do not design, modify, or independently interpret behavior plans
- RBTs direct all clinical questions to their supervisor
- RBTs do not discuss client information outside supervision or legally allowed contexts
`

